public class App {
    public static void main(String[] args) throws Exception {
        //tipo nome;
        //Variavesi do tipo inteiro
        byte numeroPequeno = 127; // vai de -128 ate 127
        System.out.println(numeroPequeno);
        
        short numeroShort = -32679; // vai de -32678 ate 32767
        System.out.println(numeroShort);
        
        int numeroMaior = 6756; // vai de 214783648 
        System.out.println(numeroMaior);
        
        long numeroEnrome = 8783468629346l; //numeor muito grande
        System.out.println(numeroEnrome);
        //Variavei do tipo flutuante
        float peso = 78.7f;
        System.out.println(peso);
        
        double pi = 3.14432;
        System.out.println(pi);
        //Variaveis do tipo caractere
        char letra = 'L' ;
        System.out.println(letra);
        //Variavel do tipo booleano
        boolean estouComFome = true;
        System.out.println(estouComFome);
        //Variavel do tipo string
        String nome = "Lucas";
        System.out.println(nome);
    }
}
